package com.llh;

public class User {

    private String name;
    private Integer age;
    private String address;

    private String aaa;
    private String ssss;

    private String getSsss;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getAaa() {
        return aaa;
    }

    public void setAaa(String aaa) {
        this.aaa = aaa;
    }

    public String getSsss() {
        return ssss;
    }

    public void setSsss(String ssss) {
        this.ssss = ssss;
    }

    public String getGetSsss() {
        return getSsss;
    }

    public void setGetSsss(String getSsss) {
        this.getSsss = getSsss;
    }
}
